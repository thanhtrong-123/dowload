    private void HienThi() {
        DefaultTableModel tableModel = (DefaultTableModel) tbDanhSachNV.getModel();
        tableModel.setColumnCount(0);
        tableModel.setRowCount(0);
        tableModel.addColumn("Mã NV");
        tableModel.addColumn("Họ Tên");
        tableModel.addColumn("Năm Sinh");
        tableModel.addColumn("Giới Tính");
        tableModel.addColumn("Địa Chỉ");
        tableModel.addColumn("SĐT");
        tableModel.addColumn("Chức Vụ");
        ArrayList<NhanVien> data = qlnv.DocDuLieu();
        for (NhanVien nv : data) {
            String chucVu = "";
            String maCV = nv.getMaCV();
            if (maCV.equals("TP")) {
                chucVu = "Trưởng Phòng";
            } else if (maCV.equals("QL")) {
                chucVu = "Quản Lý";
            } else if (maCV.equals("NV")) {
                chucVu = "Nhân Viên";
            }
            tableModel.addRow(new Object[]{nv.getMaNV(), nv.getHoTen(), nv.getNamSinh(), nv.getGioiTinh(), nv.getDiaChi(), nv.getSDT(), chucVu});
        }
    }

