    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String maNV = txtMaNV.getText();
        String hoTen = txtHoTen.getText();
        int namSinh = Integer.parseInt(txtNamSinh.getText());
        String gioiTinh = cboGioiTinh.getSelectedItem().toString();
        String diaChi = txtDiaChi.getText();
        String SDT = txtSDT.getText();
        String chucVu = cboChucVu.getSelectedItem().toString();
        String maCV = "";
        if (chucVu.equals("Trưởng Phòng")) {
            maCV = "TP";
        } else if (chucVu.equals("Quản Lý")) {
            maCV = "QL";
        } else if (chucVu.equals("Nhân Viên")) {
            maCV = "NV";
        }
        NhanVien nv = new NhanVien(maNV, hoTen, namSinh, gioiTinh, diaChi, SDT, maCV);
        qlnv.ThemDuLieu(nv);
        if (qlnv.ThemDuLieu(nv) != 0) {
            JOptionPane.showMessageDialog(this, "Thêm thất bại!");
        } else {
            JOptionPane.showMessageDialog(this, "Thêm thành công!");
        }
        HienThi();
    }//GEN-LAST:event_btnThemActionPerformed

    private void tbDanhSachNVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDanhSachNVMouseClicked
        // TODO add your handling code here:
        NhanVien nv = new NhanVien();
        int row = tbDanhSachNV.getSelectedRow();
        nv = qlnv.DocDuLieu().get(row);
        txtMaNV.setText(nv.getMaNV());
        txtHoTen.setText(nv.getHoTen());
        txtNamSinh.setText(String.valueOf(nv.getNamSinh()));
        cboGioiTinh.setSelectedItem(nv.getGioiTinh());
        txtDiaChi.setText(nv.getDiaChi());
        txtSDT.setText(nv.getSDT());
        String chucVu = "";
        String maCV = nv.getMaCV();
        if (maCV.equals("TP")) {
            chucVu = "Trưởng Phòng";
        } else if (maCV.equals("QL")) {
            chucVu = "Quản Lý";
        } else if (maCV.equals("NV")) {
            chucVu = "Nhân Viên";
        }
        cboChucVu.setSelectedItem(chucVu);
    }//GEN-LAST:event_tbDanhSachNVMouseClicked

